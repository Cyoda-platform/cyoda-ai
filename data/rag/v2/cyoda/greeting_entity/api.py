# ```python
from quart import Blueprint, request, jsonify
from app_init.app_init import entity_service, cyoda_token
from common.config.config import ENTITY_VERSION

api_bp_greeting_entity = Blueprint('api/greeting_entity', __name__)

@api_bp_greeting_entity.route('/greeting', methods=['POST'])
async def save_greeting_entity():
    """Endpoint to save a greeting entity."""
    data = await request.json

    # Validate data
    if 'name' not in data or not data['name']:
        return jsonify({"error": "Name is required."}), 400

    try:
        # Prepare greeting entity data
        greeting_entity_data = {
            "id": "generated_id",  # This would typically be generated by the DB
            "name": data["name"],
            "message": f"hello, {data['name']}",
            "request_parameters": {
                "request_method": "POST",
                "endpoint": "/greeting",
                "timestamp": "2023-10-05T14:48:00Z",  # Use actual timestamp in production
                "user_agent": request.headers.get('User-Agent')
            }
        }

        # Save the greeting entity
        greeting_entity_id = await entity_service.add_item(cyoda_token, "greeting_entity", ENTITY_VERSION, greeting_entity_data)
        
        return jsonify({"message": "Greeting entity saved successfully.", "id": greeting_entity_id}), 201

    except Exception as e:
        return jsonify({"error": str(e)}), 500
# ```
# 
# ### Explanation:
# - **Blueprint**: The `api_bp_greeting_entity` blueprint is created for routing.
# - **Route**: The `/greeting` endpoint allows users to POST a new greeting entity.
# - **Validation**: It checks if a name is provided and returns a 400 error if not.
# - **Entity Saving**: It prepares the greeting entity data, saves it using the `entity_service`, and returns a success message with the entity ID.
